let currentLocale = localStorage.getItem('locale') || 'zh';

function setLocale(locale) {
  currentLocale = locale;
  localStorage.setItem('locale', locale);
  applyTranslations();
  document.querySelectorAll('[data-locale]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.locale === locale);
  });
  window.dispatchEvent(new CustomEvent('localeChanged', { detail: { locale } }));
}

function t(key) {
  return i18n[currentLocale][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[currentLocale][key]) {
      el.textContent = i18n[currentLocale][key];
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  document.querySelectorAll('[data-locale]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.locale === currentLocale);
  });
  // Set initial active button
  updateLocaleButtons();
});

function updateLocaleButtons() {
  document.querySelectorAll('[data-locale]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.locale === currentLocale);
  });
}